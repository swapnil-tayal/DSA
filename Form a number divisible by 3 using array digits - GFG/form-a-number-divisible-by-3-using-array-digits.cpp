class Solution {
  public:
    int isPossible(int N, int arr[]) {
        // code here
        int sm = 0;
        for(int i=0; i<N; i++){
            
            while(arr[i] > 0){
                sm += arr[i]%10;
                arr[i] /= 10;
            }
        }
        // cout<<sm<<' ';
        return sm%3 == 0;
    }
};

